#include <stdio.h>

/**
 * add - function that performs addition
 * @a: numb1
 * @b: numb2
 * Return: the opration's result
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - function to performs
 *@a: num1
 *@b: num2
 *Return: the operation result
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 *mul - function to performs
 *@a: num1
 *@b: num2
 *Return: the operation result
 */
int mul(int a, int b)
{
	return (a * b);
}
/**
 *div - function to carry out division
 *@a : num 1
 *@b : num 2
 *
 *Return: double
 */
double div(int a, int b)
{
	return (a / b);
}
/**
 *mod - function to carry out division
 *@a : num 1
 *@b : num 2
 *
 *Return: int
 */
int mod(int a, int b)
{
	return (a % b);
}
